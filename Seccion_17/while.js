//Control de flujo While
// While se ejecutara hasta que toda la evaluacion siga en true si cambia a false este terminara de ejecutarse.
let x = 0
while(x <=5 ) {
    console.log(x)
    x++
}
console.log("-----------------------");
console.log("Terminando el loop");

