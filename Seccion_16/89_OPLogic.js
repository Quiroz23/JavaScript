//Operadores logicos

//or= ||
//and= &&
//not= !

//El operador logico OR si cumple la condicion siempre pasara a true tiene que tener solamente un true para pasar
const resultadoOr = false || true
/*------------- */
//El operador logico AND tiene puede ser true al tener las dos condiciones correctas si una de esta es en false automaticamente ira a false
const resultadoAnd = true && false

/*-------------- */
//El operador logico NOT cambiara el valor de la condicio es decir si es false va a true asi y viceversa
const resultadoNot = !false
console.log(resultadoNot)