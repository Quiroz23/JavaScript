//Control de flujo SWITCH
//Con SWITCH es como elegir las opciones que quieres por eso va en casos de cuantos quieras y siempre estara el default ya que si el usuario no ingresa un dato podemos decirle que no a ingresado nada.
//Tambien no olvidar poner los Breaks ya que si no lo ponemos este ejecutara tambien el default

switch(1) {
    case 1:{
        console.log("Caso 1");
        break
    }
    case 2:{
        console.log("Caso 2");
        break
    }
    case 3:{
        console.log("Caso 3");
        break
    }
    
    default:{
        console.log("Hola mundo");
        break
    }

}